package swap.business;

/**
 *
 * @author MarcoSilva
 */
public class Tuplo<L,R> {

    private L l;
    private R r;

    public Tuplo(L l, R r) {
        this.l = l;
        this.r = r;
    }

    public L getL() {
        return l;
    }

    public R getR() {
        return r;
    }

    public void setL(L l) {
        this.l = l;
    }

    public void setR(R r) {
        this.r = r;
    }
    
    
}
